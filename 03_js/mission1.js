// 숫자 변수 선언 및 할당
const number1 = 1500;
const number2 = 50;

// 덧셈 연산
const sum = number1 + number2;
console.log(`덧셈 결과: ${sum}`);

// 뺄셈 연산
const difference = number1 - number2;
console.log(`뺄셈 결과: ${difference}`);

// 곱셈 연산
const product = number1 * number2;
console.log(`곱셈 결과: ${product}`);

// 나눗셈 연산
const division = number1 / number2;
console.log(`나눗셈 결과: ${division}`);

// 나머지 연산
const remainder = 10 % 3; //10을 3으로 나눈 후의 나머지 결과는?
console.log(`나머지 결과: ${remainder}`); // 나머지 결과는 1 (몫은 3)
